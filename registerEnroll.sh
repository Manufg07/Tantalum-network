function createOrderer() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/ordererOrganizations/tantalum.com

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/ordererOrganizations/tantalum.com

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:9054 --caname ca-orderer --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-9054-ca-orderer.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-9054-ca-orderer.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-9054-ca-orderer.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-9054-ca-orderer.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/ordererOrganizations/tantalum.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy orderer org's CA cert to orderer org's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/ordererOrganizations/tantalum.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem" "${PWD}/organizations/ordererOrganizations/tantalum.com/msp/tlscacerts/tlsca.tantalum.com-cert.pem"

  # Copy orderer org's CA cert to orderer org's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/ordererOrganizations/tantalum.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem" "${PWD}/organizations/ordererOrganizations/tantalum.com/tlsca/tlsca.tantalum.com-cert.pem"

  echo "Registering orderer"
  set -x
  fabric-ca-client register --caname ca-orderer --id.name orderer --id.secret ordererpw --id.type orderer --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the orderer admin"
  set -x
  fabric-ca-client register --caname ca-orderer --id.name ordererAdmin --id.secret ordererAdminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the orderer msp"
  set -x
  fabric-ca-client enroll -u https://orderer:ordererpw@localhost:9054 --caname ca-orderer -M "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/ordererOrganizations/tantalum.com/msp/config.yaml" "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/msp/config.yaml"

  echo "Generating the orderer-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://orderer:ordererpw@localhost:9054 --caname ca-orderer -M "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/tls" --enrollment.profile tls --csr.hosts orderer.tantalum.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the orderer's tls directory that are referenced by orderer startup config
  cp "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/tls/ca.crt"
  cp "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/tls/signcerts/"* "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/tls/server.crt"
  cp "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/tls/keystore/"* "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/tls/server.key"

  # Copy orderer org's CA cert to orderer's /msp/tlscacerts directory (for use in the orderer MSP definition)
  mkdir -p "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/msp/tlscacerts"
  cp "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/tantalum.com/orderers/orderer.tantalum.com/msp/tlscacerts/tlsca.tantalum.com-cert.pem"

  echo "Generating the admin msp"
  set -x
  fabric-ca-client enroll -u https://ordererAdmin:ordererAdminpw@localhost:9054 --caname ca-orderer -M "${PWD}/organizations/ordererOrganizations/tantalum.com/users/Admin@tantalum.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/ordererOrganizations/tantalum.com/msp/config.yaml" "${PWD}/organizations/ordererOrganizations/tantalum.com/users/Admin@tantalum.com/msp/config.yaml"
}

