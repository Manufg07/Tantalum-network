#!/bin/bash

function createMiningcompany() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/miningcompany.tantalum.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-miningcompany --tls.certfiles "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-miningcompany.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-miningcompany.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-miningcompany.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-miningcompany.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy miningcompany's CA cert to miningcompany's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem" "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/msp/tlscacerts/ca.crt"

  # Copy miningcompany's CA cert to miningcompany's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem" "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/tlsca/tlsca.miningcompany.tantalum.com-cert.pem"

  # Copy miningcompany's CA cert to miningcompany's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/ca"
  cp "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem" "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/ca/ca.miningcompany.tantalum.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-miningcompany --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-miningcompany --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-miningcompany --id.name miningcompanyadmin --id.secret miningcompanyadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-miningcompany -M "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-miningcompany -M "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/tls" --enrollment.profile tls --csr.hosts peer0.miningcompany.tantalum.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/peers/peer0.miningcompany.tantalum.com/tls/server.key"

  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-miningcompany -M "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/users/User1@miningcompany.tantalum.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/users/User1@miningcompany.tantalum.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://miningcompanyadmin:miningcompanyadminpw@localhost:7054 --caname ca-miningcompany -M "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/users/Admin@miningcompany.tantalum.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/miningcompany/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/miningcompany.tantalum.com/users/Admin@miningcompany.tantalum.com/msp/config.yaml"
}

